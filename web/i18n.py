def get_language(user_language: str | None = None) -> str:
    # Always prefer explicitly set session lang (from language switcher)
    if "lang" in session and session.get("lang") in SUPPORTED:
        return session["lang"]
    lang = user_language or "sw"
    return lang if lang in SUPPORTED else "sw"


def _resolve_lang(lang: str | None = None) -> str:
    if "lang" in session and session.get("lang") in SUPPORTED:
        return session["lang"]
    chosen = lang or get_language()
    return chosen if chosen in SUPPORTED else "sw"


def brand_title(lang: str | None = None) -> str:
    current = _resolve_lang(lang)
    return TRANSLATIONS.get(current, TRANSLATIONS["sw"]).get("app_title_extra", BRAND_TITLE)


def brand_tagline(lang: str | None = None) -> str:
    current = _resolve_lang(lang)
    return TRANSLATIONS.get(current, TRANSLATIONS["sw"]).get(
        "app_tagline", BRAND_TAGLINES.get(current, BRAND_TAGLINES["sw"])
    )


def t(key: str, lang: str | None = None, **kwargs) -> str:
    current = _resolve_lang(lang)
    text = TRANSLATIONS.get(current, TRANSLATIONS["sw"]).get(
        key, TRANSLATIONS["sw"].get(key, key)
    )
    if text == key:
        if key == "app_title_extra":
            text = BRAND_TITLE
        elif key == "app_tagline":
            text = BRAND_TAGLINES.get(current, BRAND_TAGLINES["sw"])
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def role_label(role: str, lang: str | None = None) -> str:
    mapping = {
        "boss": "role_boss",
        "manager": "role_manager",
        "employee": "role_employee",
    }
    return t(mapping.get(role, role), lang)